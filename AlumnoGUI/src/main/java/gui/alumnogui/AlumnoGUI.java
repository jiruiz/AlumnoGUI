    private void chooserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooserButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileFilter filter = new FileNameExtensionFilter(null, "txt", "json");
        fileChooser.setFileFilter(filter);
        int resp = fileChooser.showOpenDialog(this);
        if (resp != JFileChooser.APPROVE_OPTION) {
            return;
        }

        fullpathTextField.setText(fileChooser.getSelectedFile().getAbsolutePath());
        try {
            Map<String, String> config = new HashMap<>();
            config.put(DAOFactory.TIPO_DAO, "TIPO_DAO_TXT");
            config.put(DAOFactory.FULLPATH, fullpathTextField.getText());
            daoTXT = (AlumnoDAOTxt) DAOFactory.getInstance().buildDAO(config);
            dao = daoTXT;

            actualizarTabla();
        } catch (DAOException | DAOFactoryException ex) {
            Logger.getLogger(AlumnoGUI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_chooserButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // Mensaje de confirmación
        //JOptionPane.showConfirmDialog(repoComboBox, evt, title, WIDTH, HEIGHT)
        int selectedRow = alumnosTable.getSelectedRow();

        switch (selectedRow) {
            case -1:
                JOptionPane.showMessageDialog(this, "Seleccioná un alumno para eliminar.", "Atención", JOptionPane.WARNING_MESSAGE);
                return;

            default:
                // Confirmación
                int confirm = JOptionPane.showConfirmDialog(
                        this,
                        "¿Estás seguro que querés eliminar este alumno?",
                        "Confirmar eliminación",
                        JOptionPane.YES_NO_OPTION
                );

                switch (confirm) {
                    case JOptionPane.YES_OPTION:
                    try {
                        Integer dniAlu = alumnos.get(selectedRow).getDni();
                        dao.delete(dniAlu); // Baja lógica
                        actualizarTabla();  // Refrescar tabla
                    } catch (DAOException ex) {
                        Logger.getLogger(AlumnoGUI.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(this, "Error al eliminar el alumno.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    break;

                    case JOptionPane.NO_OPTION:
                    default:
                        // No hacer nada si el usuario cancela
                        break;
                }
                break;
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed
        AluDialog aluDialog = new AluDialog(this, true, CrudAction.CREATE, null);
        aluDialog.setVisible(true);

        Alumno alu = aluDialog.getAlu();

        if (alu != null) {
            try {
                alu.setEstado('A');
                dao.create(alu);
                actualizarTabla();
            } catch (DAOException | PersonaException ex) {
                Logger.getLogger(AlumnoGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_crearButtonActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = alumnosTable.getSelectedRow();

        if (selectedRow != -1) {
            // Obtener el Alumno seleccionado desde la lista
            Alumno selectedAlumno = alumnos.get(selectedRow);

            // Abrir el diálogo en modo UPDATE
            AluDialog aluDialog = new AluDialog(this, true, CrudAction.UPDATE, selectedAlumno);
            aluDialog.setVisible(true);

            // Obtener el Alumno modificado desde el diálogo
            Alumno aluModificado = aluDialog.getAlu();

            try {
                if (aluModificado != null) {
                    dao.update(aluModificado);
                    actualizarTabla();
                }
            } catch (DAOException ex) {
                Logger.getLogger(AlumnoGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccioná un alumno para modificar.", "Atención", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_modificarButtonActionPerformed

    private void actualizarTabla() throws DAOException {
        boolean incluirEliminados = incluirEliminadosCheck.isSelected(); // obtenemos si está tildado
        alumnos = dao.findAll(incluirEliminados); // pasamos true o false según el checkbox
        alumnosModel.setAlumnos(alumnos);
        alumnosModel.fireTableDataChanged();
    }


